CREATE TABLE Movies (
    movie_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- Serial number (auto-generated ID)
    movie_name VARCHAR2(255) NOT NULL,  -- Movie name
    release_date DATE,  -- Date of release
    genre VARCHAR2(100),  -- Genre
    duration NUMBER(5),  -- Duration in minutes
    language VARCHAR2(50),  -- Language
    rated VARCHAR2(10),  -- Rated (family or adult)
    rating NUMBER(3, 1),  -- Rating (e.g., 8.5)
    country_of_origin VARCHAR2(100),  -- Country of origin
    director VARCHAR2(150),  -- Director's name
    star_actor VARCHAR2(150),  -- Star actor's name
    box_collection NUMBER(15, 2),  -- Box collection (e.g., in millions)
    budget NUMBER(15, 2),  -- Budget (e.g., in millions)
    awards VARCHAR2(500),  -- Awards (optional; can be multiple awards)
    release_format VARCHAR2(50),  -- Release format (e.g., Theatrical, Streaming)
    subtitle_availability VARCHAR2(3) CHECK (subtitle_availability IN ('Yes', 'No')),  -- Subtitle availability (Yes/No)
    streaming_availability VARCHAR2(255)  -- Streaming platform(s)
);
